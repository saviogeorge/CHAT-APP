//Create a model using mongoose
import mongoose from "mongoose";

const userSchema = new mongoose.Schema(
  {
    email: {
      type: String,
      required: true,
      unique: true,
    },
    fullName: {
      type: String,
      required: true,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
    profilePic: {
      type: String,
      default: "",
    },
  },
  { timestamps: true } //created at updated at
);

//By convention, class names start with a capital letter: User, Product, Order, etc.
const User = mongoose.model("User", userSchema);

export default User